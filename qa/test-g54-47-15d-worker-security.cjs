const fs=require('node:fs'),path=require('node:path');
const root=path.resolve(__dirname,'..');
const index=fs.readFileSync(path.join(root,'worker/src/index.js'),'utf8');
const sec=fs.readFileSync(path.join(root,'worker/src/security.js'),'utf8');
const wrangler=fs.readFileSync(path.join(root,'worker/wrangler.toml'),'utf8');
const report=JSON.parse(fs.readFileSync(path.join(root,'security/worker-audit-g54-47-15d.json'),'utf8'));
const checks=[]; const ok=(name,val)=>checks.push({name,ok:!!val});
const vm=index.match(/const VERSION = '([^']+)'/); ok('version',!!vm && /^G54\.\d+\.\d+(?:[A-Z]|\.\d+)?$/.test(vm[1]) );
ok('production origin required',sec.includes('ALLOW_ORIGINLESS_REQUESTS')&&index.includes('routeAllowsMissingOrigin'));
ok('content type enforced',index.includes('INVALID_CONTENT_TYPE')&&sec.includes('requireJsonContentType'));
ok('schema allowlist',sec.includes("allowedKeys = new Set"));
ok('request id sanitized',sec.includes('validRequestId')&&index.includes('validRequestId(supplied)'));
ok('production readiness gate',sec.includes('productionReadiness')&&index.includes('readiness.production'));
ok('native limiter required',sec.includes("missing.push('API_RATE_LIMITER')"));
ok('upstream error redaction',sec.includes('safeErrorCode')&&!index.includes("raw.slice(0,120)"));
ok('strict production vars',wrangler.includes('ALLOW_ORIGINLESS_REQUESTS = "false"'));
ok('open auth risk documented',report.openRisks.some(x=>x.priority==='P1'&&/App Check|ID token/.test(x.title)));
const passed=checks.filter(x=>x.ok).length; console.log(JSON.stringify({phase:(vm&&vm[1])||'UNKNOWN',passed,total:checks.length,checks},null,2)); if(passed!==checks.length)process.exit(1);
