const fs=require('fs');const path=require('path');
const root=path.resolve(__dirname,'..');let passed=0;const checks=[];
function check(name,ok){checks.push({name,ok:!!ok});if(ok)passed++;}
const backup=fs.readFileSync(path.join(root,'tools/release-backup.mjs'),'utf8');
const verify=fs.readFileSync(path.join(root,'tools/verify-backup.mjs'),'utf8');
const app=fs.readFileSync(path.join(root,'js/core/app-config.js'),'utf8');
const version=(app.match(/var VERSION = '([^']+)'/)||[])[1];
const rollbackPath=path.join(root,'docs',`ROLLBACK-${version}.md`);
const rollback=fs.readFileSync(rollbackPath,'utf8');
check('Backup-Generator vorhanden',backup.includes('SOURCE.zip')&&backup.includes('RUNTIME.zip')&&backup.includes('ROLLBACK.zip'));
check('SHA-256 Manifest',backup.includes("createHash('sha256')")&&backup.includes('BACKUP-MANIFEST.json'));
check('Leere Firebase-Exporte blockiert',backup.includes('Firebase-Export ist leer'));
check('Firebase nicht fälschlich als gesichert',backup.includes('Kein echter Firestore-Export angegeben'));
check('Backup-Ausgabe ausgeschlossen',backup.includes("'backup-output/*'"));
check('Verifier prüft SHA-256',verify.includes('sha256')&&verify.includes('Prüfsumme falsch'));
check('Rollback App dokumentiert',rollback.includes('Web-App zurücksetzen'));
check('Rollback Worker dokumentiert',rollback.includes('Cloudflare Worker zurücksetzen'));
check('Firebase Restore abgesichert',rollback.includes('nicht ungeprüft gestartet'));
check('Produktions-Sicherheitsregel',rollback.includes('Projekt-ID, Domain oder Backup-Prüfsumme'));
const result={phase:'G54.47.14C',passed,total:checks.length,ok:passed===checks.length,checks};
fs.mkdirSync(path.join(root,'release'),{recursive:true});fs.writeFileSync(path.join(root,'release/G54.47.14C_BACKUP_QA.json'),JSON.stringify(result,null,2));
console.log(JSON.stringify(result,null,2));if(!result.ok)process.exit(1);
