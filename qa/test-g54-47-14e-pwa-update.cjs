const fs=require('fs'), path=require('path');
const root=path.resolve(__dirname,'..');
const read=p=>fs.readFileSync(path.join(root,p),'utf8');
const checks=[]; const ok=(name,v)=>checks.push({name,ok:!!v});
const sw=read('service-worker.js'), mgr=read('js/core/pwa-update-manager.js'), html=read('index.html'), app=read('js/core/app-config.js');
const version=(app.match(/var VERSION = '([^']+)'/)||[])[1]||'';
ok('version',!!version&&sw.includes(version)&&mgr.includes(version));
ok('atomic core install',/CORE_ASSETS/.test(sw)&&/CORE_ASSET_FAILED/.test(sw));
ok('optional cache tolerant',/Promise\.allSettled/.test(sw));
ok('scoped cache cleanup',/key\.indexOf\(CACHE_PREFIX\)/.test(sw));
ok('navigation preload',/navigationPreload\.enable/.test(sw));
ok('network first mutable',/isMutable/.test(sw)&&/networkFirst/.test(sw));
ok('stale while revalidate assets',/staleWhileRevalidate/.test(sw));
ok('no-store update files',/update-check\\\.json/.test(sw)&&/cache: 'no-store'/.test(sw));
ok('waiting worker activation',/SKIP_WAITING/.test(sw)&&/activateUpdate/.test(mgr));
ok('controller change reload',/controllerchange/.test(mgr));
ok('updateViaCache none',/updateViaCache: 'none'/.test(mgr));
ok('manager loaded before app',html.indexOf('pwa-update-manager.js')>0&&html.indexOf('pwa-update-manager.js')<html.indexOf('<body>'));
ok('no unregister on update',!html.includes('reg.unregister()'));
ok('service worker cached',sw.includes('./js/core/pwa-update-manager.js'));
const failed=checks.filter(x=>!x.ok); console.log(JSON.stringify({phase:version,passed:checks.length-failed.length,total:checks.length,checks},null,2));
if(failed.length) process.exit(1);
