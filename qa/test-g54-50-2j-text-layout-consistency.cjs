const fs=require('fs');
const path=require('path');
const root=path.resolve(__dirname,'..');
const read=p=>fs.readFileSync(path.join(root,p),'utf8');
const checks=[]; const ok=(name,fn)=>{try{fn();checks.push({name,ok:true});}catch(e){checks.push({name,ok:false,error:e.message});}};
const assert=(v,m)=>{if(!v)throw new Error(m||'assertion failed')};
const css=read('css/text-layout-consistency.css');
const index=read('index.html');
const sw=read('service-worker.js');
const app=read('js/core/app-config.js');
ok('release version advanced to J',()=>assert(app.includes("var VERSION = 'G54.50.2J'")));
ok('layout stylesheet is loaded after deep-sheet navigation',()=>assert(index.indexOf('deep-sheet-navigation.css')<index.indexOf('text-layout-consistency.css')));
ok('service worker caches layout stylesheet',()=>assert(sw.includes('./css/text-layout-consistency.css')));
ok('unsafe anywhere wrapping is overridden',()=>assert(css.includes('overflow-wrap:break-word!important')&&css.includes('word-break:normal!important')));
ok('deep-sheet navbar uses symmetric outer columns',()=>assert(css.includes('grid-template-columns:minmax(0,1fr) minmax(220px,2fr) minmax(0,1fr)')));
ok('current title can wrap to two lines',()=>assert(css.includes('-webkit-line-clamp:2')&&css.includes('white-space:normal!important')));
ok('deep cards reserve a minmax content column',()=>assert(css.includes('grid-template-columns:auto minmax(0,1fr) auto!important')));
ok('simulation badges no longer share the title row',()=>assert(css.includes('"icon copy"')&&css.includes('"icon badge"')));
ok('track grid has responsive minimum widths',()=>assert(css.includes('repeat(auto-fit,minmax(min(100%,310px),1fr))')));
ok('information cards use minmax text columns',()=>assert(css.includes('grid-template-columns:42px minmax(0,1fr) 20px!important')));
ok('documentation and rollback exist',()=>assert(fs.existsSync(path.join(root,'docs/G54.50.2J-TEXT-LAYOUT-STABILIZATION.md'))&&fs.existsSync(path.join(root,'docs/ROLLBACK-G54.50.2J.md'))));
const failed=checks.filter(x=>!x.ok);
console.log(JSON.stringify({phase:'G54.50.2J',passed:checks.length-failed.length,total:checks.length,checks},null,2));
process.exit(failed.length?1:0);
