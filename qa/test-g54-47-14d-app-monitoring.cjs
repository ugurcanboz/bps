const fs=require('fs'),path=require('path'),assert=require('assert');
const root=path.resolve(__dirname,'..');
const read=p=>fs.readFileSync(path.join(root,p),'utf8');
const monitor=read('js/core/app-monitoring.js');
const index=read('index.html');
const sw=read('service-worker.js');
const worker=read('worker/src/index.js');
const wm=read('worker/src/monitoring.js');
const checks=[];function ok(name,fn){fn();checks.push(name);}
ok('global errors',()=>assert(monitor.includes("'javascript_error'"))); 
ok('promise rejection',()=>assert(monitor.includes("'promise_rejection'")));
ok('offline online',()=>assert(monitor.includes("addEventListener('offline'")));
ok('audio errors',()=>assert(monitor.includes("'audio_error'")));
ok('api failures',()=>assert(monitor.includes("'api_failure'")));
ok('exam lifecycle',()=>assert(monitor.includes('markExamStart')&&monitor.includes('markExamAbort')));
ok('privacy no messages',()=>assert(!/message\s*:|stack\s*:/.test(monitor)));
ok('loaded',()=>assert(index.includes('js/core/app-monitoring.js')));
ok('offline cache',()=>assert(sw.includes('js/core/app-monitoring.js')));
ok('worker ingest',()=>assert(worker.includes('/api/client-events')&&wm.includes('validateClientEvents')));
console.log(JSON.stringify({suite:'G54.47.14D app monitoring',passed:checks.length,total:10,checks},null,2));
