const fs=require('fs'); const path=require('path');
const root=path.resolve(__dirname,'..');
const reg=JSON.parse(fs.readFileSync(path.join(root,'security','threat-register-g54-47-15a.json'),'utf8'));
const md=fs.readFileSync(path.join(root,'security','G54.47.15A-BEDROHUNGSMODELL.md'),'utf8');
const checks=[]; const check=(name,ok,detail='')=>checks.push({name,ok:!!ok,detail});
check('Phase korrekt',reg.phase==='G54.47.15A');
check('Mindestens 10 Schutzwerte',reg.assets.length>=10,String(reg.assets.length));
check('Mindestens 8 Vertrauensgrenzen',reg.trustBoundaries.length>=8,String(reg.trustBoundaries.length));
check('Mindestens 20 Bedrohungen',reg.threats.length>=20,String(reg.threats.length));
check('Eindeutige IDs',new Set(reg.threats.map(x=>x.id)).size===reg.threats.length);
check('Scores korrekt',reg.threats.every(x=>x.score===x.likelihood*x.impact));
check('P0/P1-Risiken erfasst',reg.threats.some(x=>x.priority==='P0')&&reg.threats.filter(x=>x.priority==='P1').length>=10);
check('Release-Blocker erfasst',reg.releaseBlockers.length>=6,String(reg.releaseBlockers.length));
check('Folgephasen B bis J dokumentiert',['15B','15C','15D','15E','15F','15G','15H','15I','15J'].every(x=>md.includes(x)));
check('Grenzen transparent',md.includes('Kein externer Penetrationstest')&&md.includes('Keine Live-Prüfung'));
const ok=checks.every(x=>x.ok); const result={phase:reg.phase,ok,passed:checks.filter(x=>x.ok).length,total:checks.length,checks};
fs.mkdirSync(path.join(root,'release'),{recursive:true}); fs.writeFileSync(path.join(root,'release','G54.47.15A_THREAT_MODEL_QA.json'),JSON.stringify(result,null,2));
for(const c of checks) console.log(`${c.ok?'PASS':'FAIL'} ${c.name}${c.detail?' · '+c.detail:''}`);
console.log(`RESULT ${result.passed}/${result.total}`); process.exit(ok?0:1);
