const fs=require('fs'),path=require('path');
const root=path.resolve(__dirname,'..');
const read=f=>fs.readFileSync(path.join(root,f),'utf8');
const checks=[]; const ok=(name,cond)=>{checks.push({name,ok:!!cond}); if(!cond) console.error('FAIL',name)};
const dom=read('js/core/dom-security.js');
const idx=read('index.html');
const app=read('js/core/app-config.js');
const sw=read('service-worker.js');
const qa=read('js/qa/egt-qa-bypass-harness.js');
const rf=read('js/core/result-flow-engine.js');
ok('DOM security module exists',/sanitizeHTML/.test(dom)&&/safeUrl/.test(dom));
ok('Event attributes blocked',/n\.indexOf\('on'\)===0/.test(dom));
ok('javascript URLs blocked',/javascript/.test(dom));
ok('DOM security loaded before app modules',idx.indexOf('dom-security.js')>0&&idx.indexOf('dom-security.js')<idx.indexOf('result-flow-engine.js'));
ok('Result flow sanitizes dynamic HTML',/EGTDOMSecurity\.setHTML/.test(rf)&&/EGTDOMSecurity\.appendHTML/.test(rf));
ok('QA harness blocked in production',/debugAllowed\(\)/.test(qa));
ok('Production local role pins disabled',/allowLocalRolePins:\s*IS_DEVELOPMENT/.test(app));
ok('Service worker caches security module',/dom-security\.js/.test(sw));
const appVersion=(app.match(/var VERSION = '([^']+)'/)||[])[1]; const swVersion=(sw.match(/var VERSION = '([^']+?)(?:-\d{4}-\d{2}-\d{2})?';/)||[])[1];
ok('Version updated',!!appVersion&&appVersion===swVersion);
ok('No raw document.write in admin',!/w\.document\.write\(html\)/.test(read('js/admin-participant-engine.js')));
const failed=checks.filter(x=>!x.ok); console.log(JSON.stringify({suite:'G54.47.15F',passed:checks.length-failed.length,total:checks.length,checks},null,2)); process.exit(failed.length?1:0);
